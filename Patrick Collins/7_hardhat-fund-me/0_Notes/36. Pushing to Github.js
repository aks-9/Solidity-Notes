/*

* Make sure you have git installed in the system.
git --version

* 1. Initialize a git branch

git init -b main


git status
This gives you untracked file without any commits.

* 2. Stage the code for commit.

git add .


* 3. commit all the files in your project with a message.

git commit -m "initial commit"



* 4 . Adding a remote repo and name it 'origin'
git add remote origin <repo-url>

* 5. Push and fetch repos
git remote -v


* 6. Push the changes to a remote repo named 'origin' and to its 'main' branch
git push origin main
*/
